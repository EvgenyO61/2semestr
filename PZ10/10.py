# Италия,США. РейнаТур – Англия,Япония,Канада,ЮАР. Радуга – США,Испания,Швеция,
# Австралия, Италия, Канада. .Определить:
# 1. в каких турагенствах можно одновременно приобрести туры в Италию и Канаду.
# 2. в турагенство РейнаТур добавить тур в Индию.
# 3. полный список всех туров.

# Создаем словарь с турами каждого турагентства
tours = {
    'Вояж': {'Мексика', 'Канада', 'Израиль', 'Италия', 'США'},
    'РейнаТур': {'Англия', 'Япония', 'Канада', 'ЮАР'},
    'Радуга': {'США', 'Испания', 'Швеция', 'Австралия', 'Италия', 'Канада'}
}

# 1. Находим турагентства, где можно приобрести туры в Италию и Канаду одновременно
both_tours = [agency for agency, countries in tours.items() if 'Италия' in countries and 'Канада' in countries]
print("Туры в Италию и Канаду можно приобрести в следующих турагентствах:", both_tours)

